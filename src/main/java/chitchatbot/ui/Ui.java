package chitchatbot.ui;

/**
 * The UI class that contains the UI used in ChitChatBot
 */
public class Ui {

    /**
     * Greets the user during launch
     * @return String to be shown to the user during the greet
     */
    public static String greetUser() {
        return "Hello! I'm "
                + "ChitChatBot" + "\n"
                + "What can i do for you?";
    }
}
